// ==========================================
// 과목별 가이드 선택 함수
// ==========================================
export function getSubjectGuide(subject: string): string {
  const s = (subject || '').toLowerCase();
  
  if (s.includes('수학') || s.includes('math')) return MATH_GUIDE;
  if (s.includes('영어') || s.includes('english')) return ENGLISH_GUIDE;
  if (s.includes('국어') || s.includes('korean')) return KOREAN_GUIDE;
  if (s.includes('과학') || s.includes('물리') || s.includes('화학') || s.includes('생명') || s.includes('지구')) return SCIENCE_GUIDE;
  if (s.includes('사회') || s.includes('역사') || s.includes('지리') || s.includes('정치') || s.includes('경제')) return SOCIAL_GUIDE;
  
  // 기본값
  return '';
}

// ==========================================
// 학년별 난이도 조절
// ==========================================
export function getGradeLevelNote(grade: string): string {
  const g = (grade || '').toLowerCase();
  
  if (g.includes('중1')) return '(중1 수준: 기초 개념 위주, 쉬운 용어 사용)';
  if (g.includes('중2')) return '(중2 수준: 기초 + 응용, 점진적 난이도)';
  if (g.includes('중3')) return '(중3 수준: 응용 문제, 고등 연계 개념)';
  if (g.includes('고1')) return '(고1 수준: 중등 심화 + 고등 기초)';
  if (g.includes('고2')) return '(고2 수준: 고등 핵심 개념, 수능 유형)';
  if (g.includes('고3')) return '(고3 수준: 수능 대비, 심화 문제)';
  
  return '';
}


